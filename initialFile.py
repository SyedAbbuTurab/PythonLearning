# Variables
''' 
--> Variables are created when you assign a value to it 
--> Remember that variable names are case-sensitive
'''
a = 5
b = 10.5
c = True
myText = "Here is the text"


print("The value of a = ", a,
      "The value of b = ", b,
      "The value of c = ", c,
      "The value of Text = ", myText)

print(type(a))
print(type(b))
print(type(c))
print(type(myText))

#Type casting
d = str(3)
D = 3
f = float(4)

print(type(d))
print(d)
print(D)
print(type(f))

    
